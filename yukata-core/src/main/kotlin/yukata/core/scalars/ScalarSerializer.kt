package yukata.core.scalars

/**
 * Represents an abstraction class to create scalars within yukata.
 */
abstract class ScalarSerializer<T> {
    /**
     * Serializes a [object][obj] to return the value represented as [T].
     * @param obj The object to use to serialize
     * @return The [T] instance available.
     */
    abstract fun serialize(obj: Any): T
}
